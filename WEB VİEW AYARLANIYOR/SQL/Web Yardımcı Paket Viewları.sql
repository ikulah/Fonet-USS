CREATE OR REPLACE VIEW V_USW_P252_KONS AS
SELECT
          "HBS"."HASTA_GELIS_ID" AS HK_ID,
          "HH"."ID" AS DETAY_ID,
          "HH"."ID" AS ISLEM_REFERANS_NUMARASI,
          TO_CHAR(HBS.SEVK_TARIHI,'YYYYMMDDHH24MI') AS KONSULTASYON_BASLAMA_ZAMANI,
          TO_CHAR(HBS.ETAR,'YYYYMMDDHH24MI') AS KONSULTASYON_BITIS_ZAMANI,
          K1.TC_KIMLIK_NO AS KONS_ISTEYENDR_TC#Z,
          K2.TC_KIMLIK_NO AS KONS_YAPANDR_TC#Z
   FROM "FONETHBS"."HASTA_BIRIM_SEVK"/**/"HBS"
   INNER JOIN FONETHBS.HASTA_HIZMET HH ON HH.ID = HBS.SEVK_HIZMET_ID
   INNER JOIN FONETHBS.PERSONEL P1 ON P1.ID = HH.ISTEYEN_PERSONEL_ID
   INNER JOIN FONETHBS.KIMLIK K1 ON K1.ID = P1.KIMLIK_ID
   INNER JOIN FONETHBS.PERSONEL P2 ON P2.ID = HH.YAPAN_PERSONEL_ID
   INNER JOIN FONETHBS.KIMLIK K2 ON K2.ID = P2.KIMLIK_ID
   LEFT JOIN FONETHBS.KONSULTASYON K ON K.BIRIM_SEVK_ID = HBS.ID
   WHERE HBS.MUAYENE_TIPI = 6
   AND HBS.KABUL = 1;


